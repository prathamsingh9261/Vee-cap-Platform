function BlogPost({ post }) {
  const [expanded, setExpanded] = useState(false);

  return (
    <div className="card-ledger overflow-hidden">
      <div className="p-6">
        <div className="flex items-center gap-2">
          <span className="font-mono text-xs font-semibold uppercase tracking-wide text-amber-600">
            {post.category}
          </span>
          <span className="text-ink-300">•</span>
          <span className="font-mono text-xs text-ink-400">{post.date}</span>
        </div>
        <h2 className="mt-3 font-display text-2xl font-semibold leading-snug text-ink-900">
          {post.title}
        </h2>
        <p className="mt-3 text-sm leading-relaxed text-ink-600">{post.description}</p>

        {expanded && (
          <div className="mt-5 border-t border-ink-100 pt-5">
            <div className="prose prose-sm max-w-none text-ink-600">
              {post.content.split("\n\n").map((para, i) => (
                <p key={i} className="mt-3 text-sm leading-relaxed whitespace-pre-line">
                  {para}
                </p>
              ))}
            </div>
          </div>
        )}

        <button
          onClick={() => setExpanded(!expanded)}
          className="mt-4 flex items-center gap-1.5 text-sm font-semibold text-amber-600 hover:text-amber-700"
        >
          {expanded ? (
            <><ChevronUp className="h-4 w-4" /> Read Less</>
          ) : (
            <><ChevronDown className="h-4 w-4" /> Read Full Article</>
          )}
        </button>
      </div>
    </div>
  );
}

